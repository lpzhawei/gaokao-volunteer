# 从 major_track 提取大类（用于 fallback）
TRACK_CATEGORY_MAP = {
    "工": "🟡", "医": "🟡", "文": "🟡", "管": "🟡",
    "理": "⚠️", "农": "🚫", "艺": "⚠️", "预科": "⚠️",
    "港": "🟡", "军": "🟡", "警": "🔥", "试": "🟡",
}

def get_track_label(major_track: str) -> tuple:
    """返回 (emoji, 分类级别, 描述)"""
    if major_track in MAJOR_TRACK_LABELS:
        return MAJOR_TRACK_LABELS[major_track]
    # 尝试从首字推断
    if major_track:
        first = major_track[0]
        for prefix, emoji in TRACK_CATEGORY_MAP.items():
            if first.startswith(prefix):
                labels = {"🔥": "热门", "🟡": "普通", "⚠️": "谨慎", "🚫": "避坑"}
                return (emoji, labels.get(emoji, "普通"), f"{labels.get(emoji, '普通')}赛道")
    return ("🟡", "普通", "普通赛道")



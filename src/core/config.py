# 张雪峰视角：风险语言翻译（家长能听懂）
# ══════════════════════════════════════════════════════════════
def get_risk_description(prob: float) -> str:
    """将录取概率转换为张雪峰式的通俗描述"""
    if prob >= 0.90:
        return "🟢 稳妥"
    elif prob >= 0.75:
        return "🟡 较稳"
    elif prob >= 0.50:
        return "🟠 可冲"
    elif prob >= 0.25:
        return "🔴 高冲（风险较大）"
    else:
        return "⚫ 盲冲（不建议）"



def extract_province_from_name(college_name: str) -> str:
    """
    从院校名称中提取省份信息

    Args:
        college_name: 院校名称（可能包含城市信息，如"复旦大学(上海市)"）

    Returns:
        省份名称（如"上海"），如果无法识别则返回None
    """
    if not college_name:
        return None

    import re

    # 优先使用院校名称映射表
    from .college_province_mapping import get_province_by_name
    province = get_province_by_name(college_name)
    if province:
        return province

    # 1. 检查括号中的城市信息，如"复旦大学(上海市)"
    """
    从院校名称中提取省份信息
    
    Args:
        college_name: 院校名称（可能包含城市信息，如"复旦大学(上海市)"）
    
    Returns:
        省份名称（如"上海"），如果无法识别则返回None
    """
    if not college_name:
        return None
    
    import re
    
    # 1. 检查括号中的城市信息，如"复旦大学(上海市)"
    match = re.search(r'\(([^)]+[省市州自治区市旗])\)', college_name)
    if match:
        city = match.group(1)
        # 从城市映射中查找省份
        for city_key, province in CITY_TO_PROVINCE.items():
            if city_key in city or city in city_key:
                return province
    
    # 2. 检查学校名称是否包含地名（如"北京师范大学"、"上海交通大学"）
    # 常见地名关键词（匹配校名中的前缀）
    keywords = [
        ("北京", "北京"), ("上海", "上海"), ("天津", "天津"), ("重庆", "重庆"),
        ("河北", "河北"), ("山西", "山西"), ("辽宁", "辽宁"), ("吉林", "吉林"),
        ("黑龙江", "黑龙江"), ("江苏", "江苏"), ("浙江", "浙江"), ("安徽", "安徽"),
        ("福建", "福建"), ("江西", "江西"), ("山东", "山东"), ("河南", "河南"),
        ("湖北", "湖北"), ("湖南", "湖南"), ("广东", "广东"), ("广西", "广西"),
        ("海南", "海南"), ("四川", "四川"), ("贵州", "贵州"), ("云南", "云南"),
        ("西藏", "西藏"), ("陕西", "陕西"), ("甘肃", "甘肃"), ("青海", "青海"),
        ("宁夏", "宁夏"), ("新疆", "新疆"), ("内蒙古", "内蒙古"),
    ]
    
    for keyword, province in keywords:
        if college_name.startswith(keyword + "大学") or college_name.startswith(keyword + "学院"):
            return province
    
    return None


def get_province_by_code(college_code: str) -> str:
    """
    根据院校代码获取省份
    
    Args:
        college_code: 院校代码（4位数字）
    
    Returns:
        省份名称，如果无法识别则返回None
    """
    return COLLEGE_CODE_TO_PROVINCE.get(college_code)


def get_province(college_name: str = None, college_code: str = None) -> str:
    """
    获取院校所在省份（优先从代码映射，其次从名称提取）
    
    Args:
        college_name: 院校名称
        college_code: 院校代码
    
    Returns:
        省份名称，如果无法识别则返回"未知"
    """
    # 1. 优先使用院校代码映射
    if college_code:
        province = get_province_by_code(college_code)
        if province:
            return province
    
    # 2. 其次从院校名称中提取
    if college_name:
        province = extract_province_from_name(college_name)
        if province:
            return province
    
    return "未知"

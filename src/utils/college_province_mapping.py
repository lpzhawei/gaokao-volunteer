def get_province_by_name(college_name: str) -> str:
    """
    从院校名称中获取省份（使用映射表）

    Args:
        college_name: 院校名称

    Returns:
        省份名称，如果无法识别则返回None
    """
    if not college_name:
        return None

    # 清理院校名称
    clean_name = college_name.strip()
    # 去除括号中的后缀
    clean_name = re.sub(r'\([^)]*\)', '', clean_name).strip()
    # 去除方括号中的后缀
    clean_name = re.sub(r'\[[^\]]*\]', '', clean_name).strip()

    # 直接匹配
    if clean_name in COLLEGE_NAME_TO_PROVINCE:
        return COLLEGE_NAME_TO_PROVINCE[clean_name]

    # 模糊匹配（名称包含映射表中的名称）
    for name, province in COLLEGE_NAME_TO_PROVINCE.items():
        if name in clean_name or clean_name in name:
            return province

    return None


